// For loop:
for (let i = 1 ; i <= 20 ; i ++) {
  if (i % 2 == 0) {
    console.log(i + ' is even')
  } else {
    console.log(i + ' is odd')
  }
};

// While loop:
let i = 1;

while (i <= 20) {
  if (i % 2 == 0) {
    console.log(i + ' is even');
  } else {
    console.log(i + ' is odd');
  }

  i += 1;
};